"""Render evaluation/_results.json into evaluation_matrix.yaml (hand-built YAML, no pyyaml).

Thresholds for the named bias-characterisation metrics are marked n/a (no ground truth).
"""

from __future__ import annotations

import json
from pathlib import Path

EVAL = Path(__file__).resolve().parent
R = json.loads((EVAL / "_results.json").read_text())


def fnum(x, nd=4):
    if isinstance(x, bool):
        return "true" if x else "false"
    if isinstance(x, (int,)):
        return str(x)
    if isinstance(x, float):
        return f"{x:.{nd}f}"
    return f'"{x}"'


